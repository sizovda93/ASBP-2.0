export default function FaqPage() {
  const [settings, setSettings] = useState<SiteSettings>({});
  const [loaded, setLoaded] = useState(false);
  const [openItem, setOpenItem] = useState<number>(0);

  useEffect(() => {
    api.settings.getAll().then((s) => {
      setSettings(s);
      setLoaded(true);
    }).catch((err) => {
      console.error('Failed to load settings:', err);
      setLoaded(true);
    });
  }, []);

  useEffect(() => {
    if (!loaded) return;
    const els = document.querySelectorAll('.reveal');
    const obs = new IntersectionObserver(
      (entries, observer) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add('active');
            observer.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.08, rootMargin: '0px 0px -40px 0px' }
    );
    els.forEach((el) => obs.observe(el));
    return () => obs.disconnect();
  }, [loaded]);

  const navItems = settings.nav_items ? JSON.parse(settings.nav_items) : [];

  if (!loaded) return null;

  return (
    <>
      <div className="ambient-light" />
      <TopBar
        text={settings.topbar_text}
        phone={settings.topbar_phone}
        viber={settings.topbar_viber}
        whatsapp={settings.topbar_whatsapp}
        telegram={settings.topbar_telegram}
      />
      <Header
        logo={settings.nav_logo}
        ctaText={settings.nav_cta_text}
        ctaHref={settings.nav_cta_href}
        items={navItems}
      />
      <main>
        <section className="faq-page-section reveal">
          <div className="container">
            <div className="faq-page-header">
              <h1 className="faq-page-title text-gradient">Часто задаваемые вопросы</h1>
              <p className="faq-page-subtitle">
                Ответы на ключевые вопросы о партнерстве, сопровождении дел, обучении и возможностях платформы АСПБ.
              </p>
            </div>

            <div className="faq-list">
              {FAQ_ITEMS.map((item, idx) => {
                const isOpen = openItem === idx;
                return (
                  <article key={item.q} className={`faq-item ${isOpen ? 'open' : ''}`}>
                    <button
                      className="faq-trigger"
                      onClick={() => setOpenItem(isOpen ? -1 : idx)}
                      aria-expanded={isOpen}
                    >
                      <span className="faq-question">{idx + 1}. {item.q}</span>
                      <span className="faq-icon" aria-hidden="true">{isOpen ? '−' : '+'}</span>
                    </button>
                    {isOpen && (
                      <div className="faq-answer-wrap">
                        <p className="faq-answer">{item.a}</p>
                      </div>
                    )}
                  </article>
                );
              })}
            </div>
          </div>
        </section>
      </main>
      <SiteFooter
        logo={settings.nav_logo}
        description={settings.footer_description}
        copyright={settings.footer_copyright}
        phone={settings.contact_phone}
        email={settings.contact_email}
        viber={settings.footer_viber}
        whatsapp={settings.footer_whatsapp}
        telegram={settings.footer_telegram}
      />
    </>
  );
}
