interface LegalModalsProps {
  type: string | null;
  onClose: () => void;
}

export default function LegalModals({ type, onClose }: LegalModalsProps) {
  useEffect(() => {
    function handleKey(e: KeyboardEvent) {
      if (e.key === 'Escape') onClose();
    }
    if (type) {
      document.addEventListener('keydown', handleKey);
      document.body.style.overflow = 'hidden';
    }
    return () => {
      document.removeEventListener('keydown', handleKey);
      document.body.style.overflow = '';
    };
  }, [type, onClose]);

  const doc = type ? LEGAL_CONTENT[type] : null;
  if (!doc) return null;

  return (
    <div
      className={`legal-overlay${type ? ' open' : ''}`}
      onClick={(e) => {
        if (e.target === e.currentTarget) onClose();
      }}
    >
      <div className="legal-modal">
        <div className="legal-modal-header">
          <div className="legal-modal-title">{doc.title}</div>
          <button className="legal-modal-close" onClick={onClose}>
            ×
          </button>
        </div>
        <div
          className="legal-modal-body"
          dangerouslySetInnerHTML={{ __html: doc.body }}
        />
      </div>
    </div>
  );
}
